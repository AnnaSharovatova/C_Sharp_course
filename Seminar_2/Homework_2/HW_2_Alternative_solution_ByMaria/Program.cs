// Напишите программу, которая выводит случайное трехзначное число и удаляет вторую цифру этого числа.
// 456 -> 46
// 782 -> 72
// 918 -> 98

void NumRandom()
{
    int num = new Random().Next(100, 1000);
    Console.Write($"{num} -> {num / 100 * 10 + num % 10}"); // арифметический вариант: трехзначное число делим 
 на 100 (целочисленное деление) и умножаем на 10, а потом прибавляем остаток, получаем 1 цифру + 3 цифру.
}
NumRandom();

