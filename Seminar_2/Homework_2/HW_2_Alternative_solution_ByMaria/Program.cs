// Напишите программу, которая принимает на вход цифру, обозначающую день недели, и проверяет, является ли этот день выходным.
// 6 -> да
// 7 -> да
// 1 -> нет

void Weekend(int num)
{
    string text = "no"; // флаг - вводим переменную

    if (num == 6 || num ==7) text = "yes"; // в случае если заходит 6 и 7, то состояние флага меняется
    Console.WriteLine($"{num} -> {text}"); // если заходят числа, отличные от 6 и 7, то флаг остается в первоначаотном виде
}

Weekend(6);
Weekend(7);
Weekend(1);